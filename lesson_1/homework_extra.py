"""
Дополнительное задание
Структура данных "Участники спора"
Список словарей с информацией об участниках
"""

# Пустой список для участников
participants = []

print("=== Заполнение данных об участниках спора ===\n")

# Заполняем данные для 3 участников
for i in range(3):
    print(f"--- Участник №{i+1} ---")
    
    name = input("Введите наименование участника: ")
    status = input("Введите статус (Истец/Ответчик/Третье лицо): ")
    inn = input("Введите ИНН: ")
    
    # Создаём словарь для участника
    participant = {
        "name": name,
        "status": status,
        "inn": inn
    }
    
    # Добавляем словарь в список
    participants.append(participant)
    print()  # Пустая строка для разделения

# Выводим полученные данные
print("\n=== Данные об участниках спора ===\n")
print(participants)

# Более красивый вывод (для наглядности)
print("\n=== Детальный вывод ===\n")
for i, person in enumerate(participants, 1):
    print(f"Участник {i}:")
    print(f"  Наименование: {person['name']}")
    print(f"  Статус: {person['status']}")
    print(f"  ИНН: {person['inn']}")
    print()
